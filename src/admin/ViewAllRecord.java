    private void deleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblBB.getSelectedRow();
        if (selectedRow < 0) {
            return;
        }
        try {
            ObjectId id = borrowTableRowIndxWithBorrowId.get(selectedRow).getId();
            if (borrowBookDAO.checkBookWasReturned(id)) {
                JOptionPane.showMessageDialog(null, "Chưa trả sách khong the xoa");

            } else {
                if (borrowBookDAO.deleteBorrowBook(id)) {
                    this.loadAllBorrowBooks();
                    JOptionPane.showMessageDialog(null, "Xóa sach thanh cong");

                } else {
                    JOptionPane.showMessageDialog(null, "Không thể xóa sách");

                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Lỗi khi xóa sách");

        }
    }//GEN-LAST:event_deleActionPerformed

    private void seActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seActionPerformed
        // TODO add your handling code here:
        String ISBN = isbn.getText();
        String select = (String) comModelChoiceStatus.getSelectedItem();

        if (ISBN.isEmpty()) {
            List<BorrowBook> borrowBooks = borrowBookDAO.findBorrowBooksByStt(select);
            updateUITable(borrowBooks);
        } else {
            Object foundBookId = bookDAO.findIdBookByIsbn(ISBN);
            if (foundBookId == null) {
                JOptionPane.showMessageDialog(null, "Mã sách ko tồn tại hoặc chưa đúng");
            }
            try {
                List<BorrowBook> borrowBooks = borrowBookDAO.findBorrowBooksByUserIdAndBookId(select, foundBookId);
                updateUITable(borrowBooks);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Đã xảy ra lỗi khi tìm kiếm mượn sách");
            }
        }
    }
    private void updateUITable(List<BorrowBook> borrowBooks) {
        DefaultTableModel tblModel = (DefaultTableModel) tblBB.getModel();
        tblModel.setRowCount(0);
        for (int idx = 0; idx < borrowBooks.size(); idx++) {
            BorrowBook borrow = borrowBooks.get(idx);
            Object[] row = {
                idx,
                bookDAO.findBookById(borrow.getBookId()).getIsbn() + " : " + bookDAO.findBookById(borrow.getBookId()).getBookTitle(),
                userDAO.findUserById(borrow.getUserId()).getPhoneNo()+ " - " + userDAO.findUserById(borrow.getUserId()).getName(),
                DateConverter.DateFormat(borrow.getBorrowedDate().toString()),
                DateConverter.DateFormat(borrow.getDurationBorrow().toString()),
                (borrow.getReturnDate() != null) ? DateConverter.DateFormat(borrow.getReturnDate().toString()) : "0",
                borrow.getDuration(),
                borrow.getFineAmount(),
                borrow.getBorrowStatus()
            };
            tblModel.addRow(row);
        }
    }//GEN-LAST:event_seActionPerformed

