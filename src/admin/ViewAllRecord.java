    private void loadAllBorrowBooks() {
        SwingUtilities.invokeLater(() -> {
            
            while (tblModel.getRowCount() > 0) {
                tblModel.removeRow(0);
            }
            List<BorrowBook> list = borrowBookDAO.findAllBorrowBooks();
            int idx = 0;
            for (BorrowBook bb : list) {
                String re = (bb.getFineReason() != null)? bb.getFineReason() : "0";
                Object[] row = {
                    idx,
                 bb.getBook() + " - " + bb.getBook().getIsbn(),
                  bb.getUser() + " - " +  bb.getUser().getPhoneNo(),
                    DateConverter.DateFormat(bb.getBorrowedDate().toString()),
                    DateConverter.DateFormat(bb.getDurationBorrow().toString()),
                    (bb.getReturnDate() != null) ? DateConverter.DateFormat(bb.getReturnDate().toString()) : "0",
                    re,
                    String.format("%,.0f VND", bb.getFineAmount()),
                    bb.getBorrowStatus()
                };
                tblModel.addRow(row);
                borrowTableRowIndxWithBorrowId.put(idx++, bb);
            }
        });
    }

